weather = "rainy"
case (weather)
	when "sunny" then puts "You should bring your swimsuit out! The sun is GORGEOUS!"
	when "cloudy" then puts "It might be very gray in your life today... Hang in there!"
	when "rainy" then puts "Bring an umbrella with you, because storms are coming."
	when "foggy" then puts "Is this England? Because the fog is blocking my vision."
	else 
		puts "I'm sorry, I have no advice for this type of weather. Good luck!"
end
